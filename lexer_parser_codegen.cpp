// TheContext: an opaque object that owns a lot of core LLVM data structures, such as the type and 
// constant value tables. We just need a single instance to pass into APIs that require it.
static std::unique_ptr<LLVMContext> TheContext;
// TheModule: an LLVM construct that contains functions and global variables. 
// In many ways, it is the top-level structure that the LLVM IR uses to contain code. 
// It will own the memory for all of the IR that we generate, 
// which is why the codegen() method returns a raw Value*, rather than a unique_ptr<Value>.
static std::unique_ptr<Module> TheModule;
// Builder: keep track of the current place to 
// insert instructions and has methods to create new instructions.
static std::unique_ptr<IRBuilder<>> Builder;
// NamedValues: keeps track of which values are defined in the current scope and what their 
// LLVM representation is (aka, a symbol table). In this form of Kaleidoscope, the only things 
// that can be referenced are function parameters, loop induction variables and local variables.
static std::map<std::string, Value *> NamedValues;

// Log error only in the code generation stage
Value *LogErrorV(const char *Str) {
  LogError(Str);
  return nullptr;
}

// Numeric constants are represented with the ConstantFP class, which holds the numeric value in an APFloat
// internally (APFloat has the capability of holding floating point constants of Arbitrary Precision)
Value *NumberExprAST::codegen() {
  // In the LLVM IR, constants are all uniqued together and shared. 
  // For this reason, the API uses the “foo::get(…)” idiom instead of “new foo(..)” or “foo::Create(..)”.
  return ConstantFP::get(*TheContext, APFloat(Val));
}

Value *VariableExprAST::codegen() {
  // Look this variable up in the function.
  Value *V = NamedValues[Name];
  if (!V)
    return LogErrorV("Unknown variable name");
  return V;
}

Value *BinaryExprAST::codegen() {
  Value *L = LHS->codegen();
  Value *R = RHS->codegen();
  if (!L || !R)
    return nullptr;

  // LLVM instructions are constrained by strict rules: the Left and Right operands of an add instruction 
  // must have the same type, and the result type of the add must match the operand types.
  switch (Op) {
  case '+':
    // If the code emits multiple “addtmp” variables, LLVM will automatically provide each one with an 
    // increasing, unique numeric suffix. The name "addtmp" is optional, but makes it easier to read IR
    return Builder->CreateFAdd(L, R, "addtmp");
  case '-':
    return Builder->CreateFSub(L, R, "subtmp");
  case '*':
    return Builder->CreateFMul(L, R, "multmp");
  case '<':
    L = Builder->CreateFCmpULT(L, R, "cmptmp");
    // fcmp instruction always returns an ‘i1’ value (a 1-bit integer).
    // Convert bool 0/1 to double 0.0 or 1.0
    return Builder->CreateUIToFP(L, Type::getDoubleTy(*TheContext), "booltmp"); // unsigned int to float
  default:
    return LogErrorV("invalid binary operator");
  }
}

Value *CallExprAST::codegen() {
  // Look up the name in the global module table.
  Function *CalleeF = TheModule->getFunction(Callee);
  if (!CalleeF)
    return LogErrorV("Unknown function referenced");

  // If argument mismatch error.
  if (CalleeF->arg_size() != Args.size())
    return LogErrorV("Incorrect # arguments passed");

  std::vector<Value *> ArgsV;
  for (unsigned i = 0, e = Args.size(); i != e; ++i) {
    ArgsV.push_back(Args[i]->codegen());
    if (!ArgsV.back())
      return nullptr;
  }

  return Builder->CreateCall(CalleeF, ArgsV, "calltmp");
}

// This is used both for function bodies and external function declarations.
// This function returns a “Function*” instead of a “Value*”. Because a “prototype” really talks 
// about the external interface for a function (not the value computed by an expression)
Function *PrototypeAST::codegen() {
  // Make the function type:  double(double,double) etc.
  std::vector<Type *> Doubles(Args.size(), Type::getDoubleTy(*TheContext));

  // Create a function type that returns one double as a result, takes “N” doubles as arguments, 
  // and that is not vararg (false) 
  // Types in LLVM are uniqued just like Constants are, so you don’t “new” a type, you “get” it.
  FunctionType *FT = FunctionType::get(Type::getDoubleTy(*TheContext), Doubles, false);

  // Indicate type, linkage and name to use, as well as which module to insert into.
  // “external linkage” means that the function may be defined outside the current module and/or that 
  // it is callable by functions outside the module.
  // Since “TheModule” is specified, the "Name" is registered in "TheModule"s symbol table.
  Function *F = Function::Create(FT, Function::ExternalLinkage, Name, TheModule.get());

  // Set names for all arguments, making the IR more readable and allowing subsequent code to refer 
  // directly to the arguments for their names, rather than having to look up them up in the Prototype AST.
  unsigned Idx = 0;
  for (auto &Arg : F->args())
    Arg.setName(Args[Idx++]);

  return F;
}

/*
TODO:
This code does have a bug: If the FunctionAST::codegen() method finds an existing IR Function, 
it does not validate its signature against the definition’s own prototype. 
This means that an earlier ‘extern’ declaration will take precedence over the function 
definition’s signature, which can cause codegen to fail, for instance if the function arguments are 
named differently. Here is a testcase:

extern foo(a);     # ok, defines foo.
def foo(b) b;      # Error: Unknown variable name. (decl using 'a' takes precedence).
*/
Function *FunctionAST::codegen() {
  // First, check for an existing function from a previous 'extern' declaration.
  Function *TheFunction = TheModule->getFunction(Proto->getName());

  if (!TheFunction)
    TheFunction = Proto->codegen();

  if (!TheFunction)
    return nullptr;

  // Create a new basic block named "entry" to start insertion into "TheFunction"
  BasicBlock *BB = BasicBlock::Create(*TheContext, "entry", TheFunction);
  // Tell the builder that new instructions should be inserted into the end of the new basic block
  Builder->SetInsertPoint(BB);

  // Record the function arguments in the NamedValues map.
  NamedValues.clear();
  for (auto &Arg : TheFunction->args())
    NamedValues[std::string(Arg.getName())] = &Arg;

  if (Value *RetVal = Body->codegen()) {
    // Finish off the function.
    Builder->CreateRet(RetVal);

    // Validate the generated code, checking for consistency. Using this is important: it can catch a lot of bugs
    verifyFunction(*TheFunction);

    return TheFunction;
  }

  // Error reading body, remove function. Should not reach here.
  // For simplicity, we merely delete the function we produced with the eraseFromParent method.
  // This allows the user to redefine a function that they incorrectly typed in before: 
  // if we didn’t delete it, it would live in the symbol table, with a body, preventing future redefinition.
  TheFunction->eraseFromParent();
  return nullptr;
}

