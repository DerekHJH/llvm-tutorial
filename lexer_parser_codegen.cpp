//===----------------------------------------------------------------------===//
// Lexer
//===----------------------------------------------------------------------===//

// The lexer returns tokens [0-255] (its onw ASCII value such as "+") if it is an unknown character, 
// otherwise one of these for known things.
enum Token {
  tok_eof = -1,

  // commands
  tok_def = -2,
  tok_extern = -3,

  // primary
  tok_identifier = -4, // identifier: [a-zA-Z][a-zA-Z0-9]*
  tok_number = -5 // Number: [0-9.]+
};
// In real compilers, do not use global variables to store the parsed identifier and number.
static std::string IdentifierStr; // Filled in if tok_identifier
static double NumVal;             // Filled in if tok_number

/// gettok - Return the next token from standard input.
static int gettok() {
  static int LastChar = ' '; // Initialize only once.

  // Skip any whitespace.
  while (isspace(LastChar))
    LastChar = getchar(); // Make sure LastChar always point to the next character (to be processed in the next call)

  // If enter this branch, it cannot be a number
  if (isalpha(LastChar)) { 
    IdentifierStr = LastChar;
    while (isalnum((LastChar = getchar())))
      IdentifierStr += LastChar;

    if (IdentifierStr == "def")
      return tok_def;
    if (IdentifierStr == "extern")
      return tok_extern;
    return tok_identifier;
  }

  // If enter this branch, it must be a number
  /* Note that this isn’t doing sufficient error checking: 
    it will incorrectly read “1.23.45.67” and handle it as if you typed in “1.23”.
  */
  if (isdigit(LastChar) || LastChar == '.') { 
    std::string NumStr;
    do {
      NumStr += LastChar;
      LastChar = getchar();
    } while (isdigit(LastChar) || LastChar == '.');

    NumVal = strtod(NumStr.c_str(), nullptr);
    return tok_number;
  }

  if (LastChar == '#') {
    // Comment until end of line.
    do
      LastChar = getchar();
    while (LastChar != EOF && LastChar != '\n' && LastChar != '\r');

    if (LastChar != EOF)
      return gettok();
  }

  // Check for end of file.  Don't eat the EOF.
  if (LastChar == EOF)
    return tok_eof;

  // Otherwise, just return the character as its ascii value.
  int ThisChar = LastChar;
  LastChar = getchar();
  return ThisChar;
}

